<?php
  /*
     Comparison Operators
      - == equal
      - != not equal
      - > greater than
      - < less than
      - >= greater than or equal to
      - <= less than or equal to
      - === identical
      - !== not identical
  */

  /*
  $a = 5;
  $b = 5;

  echo $a == $b; // return 1
  */

  /*
  $a = 5;
  $b = 6;

  echo $a == $b; // return 0 but not show
  */

  /*
  $a = 5;
  $b = 6;

  echo $a != $b; // return 1
  */

  /*
  $a = 7;
  $b = 6;

  echo $a > $b; // return 1
  */

  /*
  $a = 7;
  $b = 6;

  echo $a >= $b; // return 1
  */

  // @@@@@@@@@@@
  /*
  $a = 5; // Integer
  $b = "5"; // String

  echo $a == $b; // return 1
  */

  /*
  $a = 5; // Integer
  $b = "5"; // string
  // data types are different!
  echo $a === $b; // return 0 but not show
  */

  $a = 5; // Integer
  $b = "5"; // string
  // data types are different!
  echo $a !== $b; // return 1
  // @@@@@@@@@@@

?>
