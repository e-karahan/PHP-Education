<?php
  /*
    Increase and Decrease Operators
    -> $a++ Increase a variable after line
    -> ++$a Increase a variable before line
    -> $a-- Decrease a variable after line
    -> --$a Decrease a variable before line
  */

  /*
  $a = 5;
  $a++;
  echo $a++; // return 6
  echo $a; // retun 7
  */

  /*
  $a = 5;
  ++$a;
  echo ++$a; // return 7
  echo ++$a; // retun 8
  */

  /*
  $a = 5;
  $a--;
  echo $a--; // return 4
  echo $a--; // retun 3
  */

  $a = 5;
  --$a;
  echo --$a; // return 3
  echo --$a; // retun 2

?>
